    [HarmonyPatch(typeof(BookModel), "GetThumbSprite")]
    class PatchThumbSprite
    {
        static void Postfix(BookModel __instance, ref Sprite __result)
        {
            if (__instance.IsWorkshop && __instance.ClassInfo.skinType == "Lor" && _map.TryGetValue(__instance.ClassInfo.GetCharacterSkin(), out var id))
            {
                __result = Resources.Load<Sprite>("Sprites/Books/Thumb/" + id);
            }
        }
    }
